void function_g(unsigned char byte[4], int counter) {
  const unsigned char Rcon[10] = {0x01, 0x02, 0x04, 0x08, 0x10,
                                  0x20, 0x40, 0x80, 0x1B, 0x36};

  unsigned char tmp;

  tmp = byte[0];
  byte[0] = byte[1];
  byte[1] = byte[2];
  byte[2] = byte[3];
  byte[3] = tmp;

  byte[0] = subbytes(byte[0]);
  byte[1] = subbytes(byte[1]);
  byte[2] = subbytes(byte[2]);
  byte[3] = subbytes(byte[3]);

  byte[0] ^= Rcon[counter];
}

static unsigned char round_keys[44][4];

void keyexpantion(unsigned char key[4][4], unsigned char w[44][4]) {

  // 初期鍵をW[0]〜W[3]にコピー（列単位）
  for (int i = 0; i < 4; i++) {
    for (int j = 0; j < 4; j++) {
      w[i][j] = key[j][i];
    }
  }

  unsigned char temp[4];
  int i = 4;
  while (i < 44) {
    for (int j = 0; j < 4; j++) {
      temp[j] = w[i - 1][j];
    }

    if (i % 4 == 0) {
      function_g(temp, (i / 4) - 1);
    }

    for (int j = 0; j < 4; j++) {
      w[i][j] = w[i - 4][j] ^ temp[j];
    }
    i++;
  }
}

void get_roundkey(unsigned char roundkey[4][4], unsigned char w[44][4],
                  int round) {
  for (int col = 0; col < 4; col++) {
    for (int row = 0; row < 4; row++) {
      roundkey[row][col] = w[round * 4 + col][row];
    }
  }
}

void setkey(unsigned char *key) {
  unsigned char key_matrix[4][4];
  for (int col = 0; col < 4; col++) {
    for (int row = 0; row < 4; row++) {
      key_matrix[row][col] = key[col * 4 + row];
    }
  }
  keyexpantion(key_matrix, round_keys);
}

void print_block(const char *label, const unsigned char *data) {
  printf("%s: ", label);
  for (int i = 0; i < 16; i++) {
    printf("%02x ", data[i]);
  }
  printf("\n");
}

int main() {

  unsigned char state[4][4];
  unsigned char round_key[4][4];
  unsigned char output[16];

  for (int col = 0; col < 4; col++) {
    for (int row = 0; row < 4; row++) {
      state[row][col] = input[col * 4 + row];
    }
  }

  setkey(key);

  get_roundkey(round_key, round_keys, 10);
  addroundkey(state, round_key);

  for (int round = 9; round >= 1; --round) {

    inv_shiftrows(state);

    for (int k = 0; k < 4; k++) {
      for (int l = 0; l < 4; l++) {
        state[k][l] = inv_subbytes(state[k][l]);
      }
    }

    get_roundkey(round_key, round_keys, round);
    addroundkey(state, round_key);

    for (int c = 0; c < 4; c++) {
      inv_mixcolumns(state, c);
    }
  }

  inv_shiftrows(state);
  for (int k = 0; k < 4; k++) {
    for (int l = 0; l < 4; l++) {
      state[k][l] = inv_subbytes(state[k][l]);
    }
  }

  get_roundkey(round_key, round_keys, 0);
  addroundkey(state, round_key);

  for (int col = 0; col < 4; col++) {
    for (int row = 0; row < 4; row++) {
      output[col * 4 + row] = state[row][col];
    }
  }

  print_block("plain", output);

  return 0;
}